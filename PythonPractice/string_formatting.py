# Storing a lengthy text into a variable - Using \

about_me = "I am Sainadh Reddy," \
           "a Software Test Engineer with a keen interest in Automation Testing." \
           "Here, I am enthusiastic about sharing my knowledge with all of you."

print(about_me)

about_me = "I am Sainadh Reddy,\n"\
           "a Software Test Engineer with a keen interest in Automation Testing.\n" \
           "Here, I am enthusiastic about sharing my knowledge with all of you."

print(about_me)